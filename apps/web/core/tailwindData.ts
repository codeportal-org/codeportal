export type TailwindClassData = {
  name: string
  title: string
  description: string
  type:
    | "sizing"
    | "spacing"
    | "layout"
    | "typography"
    | "borders"
    | "interactivity"
    | "svg"
    | "backgrounds"
  docsUrl: string
  /**
   * A function that returns the generated CSS for the given value and modifier.
   * Used for showing the generated CSS in the UI.
   */
  genFn?: (value: any, modifier?: any) => string

  useScale?: boolean
  useFractions?: boolean
  useColors?: boolean
  values?: string[]
  prefixes?: string[]
  isStandalone?: boolean
  /**
   * Whether the value can be arbitrary or not.
   */
  allowArbitraryValues: boolean
  arbitraryValueType?: "dimension" | "color" | "string"
}

export type TailwindClassDataItem = {
  className: string
  args?: string[]
  prefix?: string
  data: TailwindClassData
}

export const generateTailwindClassesData = (): TailwindClassDataItem[] => {
  const classesData: TailwindClassDataItem[] = []

  for (const tailwindStyle of tailwindData) {
    const tailwindStyleClasses: TailwindClassDataItem[] = []

    if (tailwindStyle.values) {
      for (const value of tailwindStyle.values) {
        const item: TailwindClassDataItem = {
          className: `${tailwindStyle.name}-${value}`,
          args: [value],
          data: tailwindStyle,
        }

        tailwindStyleClasses.push(item)
      }
    }

    if (tailwindStyle.isStandalone) {
      const item: TailwindClassDataItem = {
        className: tailwindStyle.name,
        data: tailwindStyle,
      }

      tailwindStyleClasses.push(item)
    }

    if (tailwindStyle.useScale) {
      for (const value of tailwindScale) {
        const item: TailwindClassDataItem = {
          className: `${tailwindStyle.name}-${value}`,
          args: [value],
          data: tailwindStyle,
        }

        tailwindStyleClasses.push(item)
      }
    }

    if (tailwindStyle.useFractions) {
      for (const value of tailwindFractions) {
        const item: TailwindClassDataItem = {
          className: `${tailwindStyle.name}-${value}`,
          args: [value],
          data: tailwindStyle,
        }

        tailwindStyleClasses.push(item)
      }
    }

    if (tailwindStyle.useColors) {
      for (const color of tailwindRawColors) {
        const item: TailwindClassDataItem = {
          className: `${tailwindStyle.name}-${color}`,
          args: [color],
          data: tailwindStyle,
        }

        tailwindStyleClasses.push(item)
      }

      for (const color of tailwindPaletteColors) {
        for (const shade of tailwindPaletteShades) {
          const item: TailwindClassDataItem = {
            className: `${tailwindStyle.name}-${color}-${shade}`,
            args: [color, shade],
            data: tailwindStyle,
          }

          tailwindStyleClasses.push(item)
        }
      }
    }

    if (tailwindStyle.allowArbitraryValues) {
      const arbitraryColorItem: TailwindClassDataItem = {
        className: `${tailwindStyle.name}-[arbitrary_${tailwindStyle.arbitraryValueType}]`,
        data: tailwindStyle,
      }

      tailwindStyleClasses.push(arbitraryColorItem)
    }

    const additionalStyleClasses: TailwindClassDataItem[] = []

    if (tailwindStyle.prefixes) {
      for (const prefix of tailwindStyle.prefixes) {
        for (const className of tailwindStyleClasses) {
          const item: TailwindClassDataItem = {
            className: `${prefix}-${className.className}`,
            args: className.args,
            prefix,
            data: tailwindStyle,
          }

          additionalStyleClasses.push(item)
        }
      }
    }

    classesData.push(...tailwindStyleClasses)
    classesData.push(...additionalStyleClasses)
  }

  return classesData
}

